		<ul>
			<li><strong>Job: </strong>Heads the web development team at a mid-sized tech company.</li>
			<li><strong>Technology: </strong>Desktop computer at the office.</li>
			<li><strong>Behaviors and Attitudes: </strong>Larry is very results-oriented and more interested in the
				quality of someone's work than whether they have a degree in the field.</li>
			<li><strong>Frustrations and Needs: </strong>Larry is frustrated by not finding enough developers focused on
				making responsive mobile design. For this reason he may view the website on his mobile phone.</li>
		</ul>
		<h2>Use Case</h2>
		<p><strong>Scenario: </strong>Larry has received my business card or resume with a link to my website,
			and is visiting the site to see what I can do. He is primarily interested in the gallery of my recent projects.
			He is evaluating me as candidate for a job and deciding whether to call for an interview.
			Larry only has a short time to evaluate my website, because he has to evaluate 5 other candidates.
		</p>
		<p>
			Larry is viewing the site on his desktop computer at the office, using Chrome. Here is an outline of
			his interaction with my site:
		</p>
		<ol>
			<li>Larry lands on my home/about me page and skims the brief summary of my core skills.</li>
			<li>From this page he uses the navigation menu at the top to view the gallery of my past projects,
				and decides that my work looks good.</li>
			<li>If he has time, he will view the website on his mobile phone and maybe play the game on my games page
				to see what I am capable of as a programmer. </li>
			<li>He uses the contact information in the footer of the page (it is at the foot of all pages on the site)
				to send me an email and schedule a phone interview.</li>
		</ol>
		<h2>Proposed Site Map</h2>
		<p>The site will consist of 3 top-level pages, with no sub-pages.</p>
		<ol>
			<li><strong>A home/about me page</strong>, detailing my qualifications for a job as a web developer, my programming languages,
				and some professional history. This page will be scrolling and responsive; I want to try out a parallax
				effect here with a high resolution image. At the bottom of the page I will include a small contact form.</li>
			<li><strong>A gallery </strong>with up to 8 images of my past work, including links to the pages and a brief
				description of my role. This will include a link to my capstone project.</li>
			<li><strong>A games page</strong> to include the Collapse game I made (stretch goal) and a link to my Github
				repositories, which will demonstrate my coding abilities.</li>
		</ol>
		<h2>Proposed Features and Functionality</h2>
		<ul>
			<li>A navbar at the top of each page. This will make use of the Bootstrap pill buttons</li>
			<li>Contact form at the bottom of the main page</li>
			<li>Contact information in the footer of all pages</li>
			<li>A gallery/slideshow of screenshots of various projects, using the bootstrap responsive embed feature so it
			will scale for mobile devices</li>
			<li>Responsive background imagery</li>
			<li>An embedded game on the games page for people to play (or possibly just a download link)</li>
			<li></li>
		</ul>
